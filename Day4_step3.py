# 전체 이력 출력 함수
# ============================================
def 전체이력출력(기록):
    """환자전체기록조회 결과를 보기좋게 출력한다."""
    환자 = 기록["환자"]
    print(f"\n{'='*50}")
    print(f" 환자명: {환자['이름']}  |  생년월일: {환자['생년월일']}  |  성별: {환자['성별']}")
    if 환자['가족력']:
        print(f" 가족력: {환자['가족력']}")
    if 환자['약부작용이력']:
        print(f" 약부작용이력: {환자['약부작용이력']}")
    print(f"{'='*50}")

    # 진단 목록
    print(f"\n--- 진단 ({len(기록['진단'])}건) ---")
    if 기록["진단"]:
        for d in 기록["진단"]:
            방문일표시 = f" (방문일: {d['방문일']})" if d.get('방문일') else ""
            print(f"  [{d['상태']}] {d['진단명']}{방문일표시}")
            if d['비고']:
                print(f"        비고: {d['비고']}")
    else:
        print("  (없음)")

    # 방문 기록
    print(f"\n--- 방문 기록 ({len(기록['방문'])}건) ---")
    if 기록["방문"]:
        for v in 기록["방문"]:
            판정 = 혈압판정(v['수축기'], v['이완기'])
            print(f"  [{v['방문일']}] BP {v['수축기']}/{v['이완기']} ({판정}) | HR {v['심박수']}")
            print(f"    키: {v['키']}cm  몸무게: {v['몸무게']}kg  BMI: {v['BMI']}")
            if v['흡연'] or v['음주'] or v['운동']:
                print(f"    흡연: {v['흡연']}  음주: {v['음주']}  운동: {v['운동']}")
            if v['free_text']:
                print(f"    메모: {v['free_text']}")
            if v['처방요약']:
                print(f"    처방: {v['처방요약']}")
    else:
        print("  (없음)")

    # 검사결과
    print(f"\n--- 혈액검사 ({len(기록['검사결과'])}건) ---")
    if 기록["검사결과"]:
        for l in 기록["검사결과"]:
            참고 = f" (참고: {l['참고범위']})" if l['참고범위'] else ""
            print(f"  [{l['검사시행일']}] {l['검사항목']}: {l['결과값']} {l['단위']}{참고}")
    else:
        print("  (없음)")

    # 영상검사
    print(f"\n--- 영상검사 ({len(기록['영상검사'])}건) ---")
    if 기록["영상검사"]:
        for img in 기록["영상검사"]:
            print(f"  [{img['검사시행일']}] {img['검사종류']}: {img['결과요약']}")
            if img['주요수치']:
                print(f"        주요수치: {img['주요수치']}")
    else:
        print("  (없음)")

    # 추적계획
    print(f"\n--- 추적계획 ({len(기록['추적계획'])}건) ---")
    if 기록["추적계획"]:
        for p in 기록["추적계획"]:
            완료 = "✓" if p['완료여부'] else "○"
            print(f"  [{완료}] {p['예정일']} — {p['내용']}")
    else:
        print("  (없음)")

    print("")


# ============================================
# 메인 루프
# ============================================
while True:
    명령 = input("명령 입력: ").strip()

    if 명령 == "끝":
        print("프로그램을 종료합니다.")
        break

    # ---- 신환등록 ----
    elif 명령 == "신환등록":
        이름 = input(" 환자 이름: ").strip()
        생년월일 = input(" 생년월일 (YYYY-MM-DD): ").strip()
        성별 = input(" 성별 (M/F): ").strip()
        가족력 = input(" 가족력 (없으면 Enter): ").strip()
        약부작용이력 = input(" 약부작용이력 (없으면 Enter): ").strip()

        환자id = 환자등록(이름, 생년월일, 성별, 가족력, 약부작용이력)
        전체수 = len(환자목록가져오기())
        print(f"\n {이름} 환자 등록 완료! (환자id: {환자id}, 총 {전체수}명)\n")

    # ---- 방문기록 ----
    elif 명령 == "방문기록":
        검색어 = input(" 환자 이름: ").strip()
        결과 = 환자검색(검색어)

        if not 결과:
            print(f" '{검색어}' 환자를 찾을 수 없습니다.\n")
            continue

        # 여러 명이면 선택
        if len(결과) > 1:
            print(f"\n {len(결과)}명의 환자가 검색되었습니다:")
            for i, 환자 in enumerate(결과, 1):
                print(f"  {i}. {환자['이름']} ({환자['생년월일']}, {환자['성별']})")
            선택 = 숫자입력("환자 번호 선택: ", 1, len(결과))
            선택환자 = 결과[선택 - 1]
        else:
            선택환자 = 결과[0]
            print(f" -> {선택환자['이름']} ({선택환자['생년월일']}) 선택됨")

        환자id = 선택환자["환자id"]

        # 방문 기본정보 입력
        방문일 = input(" 방문일 (YYYY-MM-DD): ").strip()
        수축기 = 숫자입력(" 수축기 혈압: ", 50, 250)
        이완기 = 숫자입력(" 이완기 혈압: ", 30, 180)
        print(f" -> 혈압 판정: {혈압판정(수축기, 이완기)}")
        심박수 = 숫자입력(" 심박수: ", 30, 200)
        키 = float(input(" 키 (cm): "))
        몸무게 = float(input(" 몸무게 (kg): "))
        BMI = round(몸무게 / (키 / 100) ** 2, 1)
        print(f" -> BMI: {BMI}")

        흡연 = input(" 흡연 (없으면 Enter): ").strip()
        음주 = input(" 음주 (없으면 Enter): ").strip()
        운동 = input(" 운동 (없으면 Enter): ").strip()
        free_text = input(" 메모/소견 (없으면 Enter): ").strip()
        처방요약 = input(" 처방요약 (없으면 Enter): ").strip()

        방문id = 방문기록추가(환자id, 방문일, 수축기, 이완기, 심박수, 키, 몸무게, BMI, 흡연, 음주, 운동, free_text, 처방요약)
        print(f"\n 방문 기록 저장 완료! (방문id: {방문id})")

        # 진단 추가
        while input("\n 진단을 추가하시겠습니까? (y/n): ").strip().lower() == "y":
            진단명 = input("  진단명: ").strip()
            상태 = input("  상태 (활성/관해/종결, 기본: 활성): ").strip() or "활성"
            비고 = input("  비고 (없으면 Enter): ").strip()
            진단추가(환자id, 방문id, 진단명, 상태, 비고)
            print("  -> 진단 추가 완료!")

        # 검사결과 추가
        while input("\n 혈액검사 결과를 추가하시겠습니까? (y/n): ").strip().lower() == "y":
            검사시행일 = input("  검사시행일 (YYYY-MM-DD): ").strip()
            검사항목 = input("  검사항목 (예: HbA1c, LDL): ").strip()
            결과값 = input("  결과값: ").strip()
            단위 = input("  단위 (예: mg/dL): ").strip()
            참고범위 = input("  참고범위 (예: 70-100): ").strip()
            검사결과추가(환자id, 검사시행일, 검사항목, 결과값, 단위, 참고범위)
            print("  -> 검사결과 추가 완료!")

        # 영상검사 추가
        while input("\n 영상검사 결과를 추가하시겠습니까? (y/n): ").strip().lower() == "y":
            검사시행일 = input("  검사시행일 (YYYY-MM-DD): ").strip()
            검사종류 = input("  검사종류 (예: 흉부X-ray, CT): ").strip()
            결과요약 = input("  결과요약: ").strip()
            주요수치 = input("  주요수치 (없으면 Enter): ").strip()
            영상검사추가(환자id, 검사시행일, 검사종류, 결과요약, 주요수치)
            print("  -> 영상검사 추가 완료!")

        # 추적계획 추가
        while input("\n 추적계획을 추가하시겠습니까? (y/n): ").strip().lower() == "y":
            예정일 = input("  예정일 (YYYY-MM-DD): ").strip()
            내용 = input("  내용: ").strip()
            추적계획추가(환자id, 방문id, 예정일, 내용)
            print("  -> 추적계획 추가 완료!")

        print("")

    # ---- 검색 ----
    elif 명령 == "검색":
        검색어 = input(" 환자 이름: ").strip()
        결과 = 환자검색(검색어)

        if not 결과:
            print(f" '{검색어}' 환자를 찾을 수 없습니다.\n")
            continue

        # 여러 명이면 선택
        if len(결과) > 1:
            print(f"\n {len(결과)}명의 환자가 검색되었습니다:")
            for i, 환자 in enumerate(결과, 1):
                print(f"  {i}. {환자['이름']} ({환자['생년월일']}, {환자['성별']})")
            선택 = 숫자입력("조회할 환자 번호: ", 1, len(결과))
            선택환자 = 결과[선택 - 1]
        else:
            선택환자 = 결과[0]

        # 전체 이력 조회 및 출력
        기록 = 환자전체기록조회(선택환자["환자id"])
        전체이력출력(기록)

    # ---- 통계 ----
    elif 명령 == "통계":
        통계보기()

    else:
        print(" 알 수 없는 명령입니다. '신환등록', '방문기록', '검색', '통계', '끝' 중 하나를 입력하세요.")
        print("")
