# SQLite를 활용한 환자 저장/검색 시스템
from util import 혈압판정, 환자브리핑, 숫자입력, 통계보기, 환자등록, 환자목록가져오기, 환자검색, 환자삭제, 환자수정

# 프로그램 시작
print("환자 등록 시스템 (SQLite DB 기반, 데이터는 즉시 저장됩니다)")
print("")

# DB에 저장된 환자 수 확인
기존환자 = 환자목록가져오기()
print(f"-> DB에 {len(기존환자)}명의 환자 데이터가 있습니다.")
print("")

# 사용할 수 있는 명령어를 알려줌
print("명령어: '등록' / '삭제' / '수정' / '목록' / '검색' / '통계' / '끝'")
print("")

# 메인 루프: 프로그램 종료 전까지 명령을 계속 받는다
while True:
    명령 = input("명령 입력: ")

    if 명령 == "끝":
        # SQLite는 매 작업마다 바로 저장되므로 별도 저장이 필요 없다
        print("프로그램을 종료합니다.")
        break

    elif 명령 == "등록":
        이름 = input(" 환자 이름: ")
        나이 = 숫자입력("나이: ", 0, 130)
        진단 = input("진단명: ")
        while True:
            수축기 = 숫자입력("수축기 혈압: ", 0, 250)
            이완기 = 숫자입력("이완기 혈압: ", 0, 180)
            if 수축기 > 이완기:
                break
            else:
                print("이완기 혈압이 수축기 혈압보다 클 수 없습니다. 다시 입력하세요.")

        # DB에 바로 저장 (JSON처럼 전체를 다시 쓸 필요 없음!)
        환자등록(이름, 나이, 진단, 수축기, 이완기)
        전체수 = len(환자목록가져오기())
        print(f" {이름} 환자 등록 완료! (총 {전체수}명)")
        print("")

    elif 명령 == "목록":
        # DB에서 전체 환자 목록을 가져온다
        목록 = 환자목록가져오기()
        if len(목록) == 0:
            print("등록된 환자가 없습니다.")
        else:
            print(f"\n===전체 환자 목록({len(목록)}명) ===\n")
            for i, 환자 in enumerate(목록, 1):
                print(f"{i}번 환자")
                환자브리핑(환자)
        print("")

    elif 명령 == "검색":
        검색어 = input(" 환자 이름: ")
        # DB에서 이름으로 검색
        결과 = 환자검색(검색어)
        if len(결과) > 0:
            print("")
            print("=== 검색 결과 ===")
            for 환자 in 결과:
                환자브리핑(환자)
        else:
            print(f" '{검색어}' 환자를 찾을 수 없습니다.")
        print("")

    elif 명령 == "삭제":
        검색어 = input(" 삭제할 환자 이름: ")
        # DB에서 바로 삭제
        삭제수 = 환자삭제(검색어)
        if 삭제수 > 0:
            전체수 = len(환자목록가져오기())
            print(f"\n{검색어} 환자를 삭제하였습니다.\n")
            print(f"현재 목록에 ({전체수})명의 환자가 있습니다.")
        else:
            print("삭제할 환자가 없습니다.")
        print("")

    elif 명령 == "통계":
        # DB에서 직접 통계를 계산한다 (인자 없음)
        통계보기()

    elif 명령 == "수정":
        검색어 = input(" 수정할 환자 이름: ")
        # 먼저 해당 환자가 있는지 확인
        결과 = 환자검색(검색어)
        if len(결과) > 0:
            수축기 = int(input("수정할 수축기 혈압: "))
            이완기 = int(input("수정할 이완기 혈압: "))
            # DB에서 바로 수정
            환자수정(검색어, 수축기, 이완기)
            print(f"\n {검색어} 환자의 혈압을 수정하였습니다. \n")
        else:
            print("수정할 환자가 없습니다.")
        print("")

    else:
        print(" 알 수 없는 명령입니다. '등록', '삭제', '수정', '목록', '검색', '통계', '끝' 중 하나를 입력하세요.")
        print("")
