def 전체이력출력(기록):
    """환자전체기록조회 결과를 보기좋게 출력한다."""
    환자 = 기록["환자"]
    나이 = 나이계산(환자['생년월일'])
    나이표시 = f"  |  나이: {나이}세" if 나이 is not None else ""

    print(f"\n{'='*50}")
    print(f" 환자명: {환자['이름']}  |  생년월일: {환자['생년월일']}{나이표시}  |  성별: {환자['성별']}")
    if 환자['가족력']:
        print(f" 가족력: {환자['가족력']}")
    if 환자['약부작용이력']:
        print(f" 약부작용이력: {환자['약부작용이력']}")
    print(f"{'='*50}")

    # 진단 목록
    print(f"\n--- 진단 ({len(기록['진단'])}건) ---")
    if 기록["진단"]:
        for d in 기록["진단"]:
            방문일표시 = f" (방문일: {d['방문일']})" if d.get('방문일') else ""
            print(f"  [진단id:{d['진단id']}] [{d['상태']}] {d['진단명']}{방문일표시}")
            if d['비고']:
                print(f"        비고: {d['비고']}")
    else:
        print("  (없음)")

    # 방문 기록
    print(f"\n--- 방문 기록 ({len(기록['방문'])}건) ---")
    if 기록["방문"]:
        for v in 기록["방문"]:
            판정 = 혈압판정(v['수축기'], v['이완기'])
            print(f"  [방문id:{v['방문id']}] [{v['방문일']}] BP {v['수축기']}/{v['이완기']} ({판정}) | HR {v['심박수']}")
            print(f"    키: {v['키']}cm  몸무게: {v['몸무게']}kg  BMI: {v['BMI']}")
            if v['흡연'] or v['음주'] or v['운동']:
                print(f"    흡연: {v['흡연']}  음주: {v['음주']}  운동: {v['운동']}")
            if v['free_text']:
                print(f"    메모: {v['free_text']}")
            if v['처방요약']:
                print(f"    처방: {v['처방요약']}")
    else:
        print("  (없음)")

    # 검사결과
    print(f"\n--- 혈액검사 ({len(기록['검사결과'])}건) ---")
    if 기록["검사결과"]:
        for l in 기록["검사결과"]:
            참고 = f" (참고: {l['참고범위']})" if l['참고범위'] else ""
            print(f"  [검사id:{l['검사id']}] [{l['검사시행일']}] {l['검사항목']}: {l['결과값']} {l['단위']}{참고}")
    else:
        print("  (없음)")

    # 영상검사
    print(f"\n--- 영상검사 ({len(기록['영상검사'])}건) ---")
    if 기록["영상검사"]:
        for img in 기록["영상검사"]:
            print(f"  [영상id:{img['영상id']}] [{img['검사시행일']}] {img['검사종류']}: {img['결과요약']}")
            if img['주요수치']:
                print(f"        주요수치: {img['주요수치']}")
    else:
        print("  (없음)")

    # 추적계획
    print(f"\n--- 추적계획 ({len(기록['추적계획'])}건) ---")
    if 기록["추적계획"]:
        for p in 기록["추적계획"]:
            완료 = "✓" if p['완료여부'] else "○"
            print(f"  [추적id:{p['추적id']}] [{완료}] {p['예정일']} — {p['내용']}")
    else:
        print("  (없음)")

    print("")


