# 환자 선택 공통 함수
# ============================================
def 환자선택(안내문="환자 이름: "):
    """이름으로 환자를 검색하고 선택하여 환자 dict를 반환한다. 실패 시 None."""
    검색어 = input(f" {안내문}").strip()
    결과 = 환자검색(검색어)

    if not 결과:
        print(f" '{검색어}' 환자를 찾을 수 없습니다.\n")
        return None

    if len(결과) > 1:
        print(f"\n {len(결과)}명의 환자가 검색되었습니다:")
        for i, 환자 in enumerate(결과, 1):
            나이 = 나이계산(환자['생년월일'])
            나이표시 = f", {나이}세" if 나이 is not None else ""
            print(f"  {i}. {환자['이름']} ({환자['생년월일']}, {환자['성별']}{나이표시})")
        선택 = 숫자입력(" 환자 번호 선택: ", 1, len(결과))
        return 결과[선택 - 1]
    else:
        환자 = 결과[0]
        나이 = 나이계산(환자['생년월일'])
        나이표시 = f", {나이}세" if 나이 is not None else ""
        print(f" -> {환자['이름']} ({환자['생년월일']}{나이표시}) 선택됨")
        return 환자


