# 함수 = 동작 세트를 미리 만들어 두는 것트(약속 처방 같은 것)

# 함수: "혈압 판정"을 해주는 세트
def 혈압판정(수축기, 이완기):
    if 수축기 >= 180 or 이완기 >= 120:
        return "!!!!고혈압성 응급!!!!즉시 조치 요함!!!!"
    elif 수축기 >=140 or 이완기 >= 90:
        return "2기 고혈압. 치료를 요함."
    elif 수축기 >= 130 or 이완기 >= 80:
        return "1기 고혈압, 식습관 조졀을 요함."
    elif 수축기 >= 120:
        return "주의: 혈압 상승. 정기적 추적을 요함."
    else:
        return "정상 혈압"
    
# 함수: 환자 브리핑용 세트
def 환자브리핑(환자):
    print("=== 환자 브리핑 ===")
    print("이름:", 환자["이름"])
    print("나이:", 환자["나이"],"세")
    print("진단:", 환자["진단"])
    print("혈압:", 환자["수축기"], "/", 환자["이완기"], "mmHg")
    print("판정:", 혈압판정(환자["수축기"], 환자["이완기"]))
    print("")

