# 핵심 함수 2: 분석결과 확인 (의사 동의/반려)
# ============================================
def 분석결과_확인(분석결과):
    """AI 분석 결과를 의사에게 보여주고, 동의한 항목만 반환한다."""
    확인결과 = {}

    print("\n=== AI 분석 결과 ===\n")

    # --- 활력징후 ---
    활력 = 분석결과.get("활력징후", {})
    if any(활력.get(k) for k in ["수축기", "이완기", "심박수", "키", "몸무게"]):
        bp = f"BP {활력.get('수축기', '?')}/{활력.get('이완기', '?')}-{활력.get('심박수', '?')}"
        체중 = f", 체중 {활력.get('몸무게', '?')}kg" if 활력.get('몸무게') else ""
        키표시 = f", 키 {활력.get('키', '?')}cm" if 활력.get('키') else ""
        bmi = f", BMI {활력.get('BMI', '?')}" if 활력.get('BMI') else ""
        print(f" [활력징후] {bp}{키표시}{체중}{bmi}")
        if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
            확인결과["활력징후"] = 활력

    # --- 생활습관 ---
    생활 = 분석결과.get("생활습관", {})
    if any(생활.get(k) for k in ["흡연", "음주", "운동"]):
        항목들 = []
        if 생활.get("흡연"):
            항목들.append(f"흡연: {생활['흡연']}")
        if 생활.get("음주"):
            항목들.append(f"음주: {생활['음주']}")
        if 생활.get("운동"):
            항목들.append(f"운동: {생활['운동']}")
        print(f" [생활습관] {', '.join(항목들)}")
        if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
            확인결과["생활습관"] = 생활

    # --- 진단 ---
    진단들 = 분석결과.get("진단", [])
    확인된_진단 = []
    if 진단들:
        print("\n [진단]")
        for d in 진단들:
            구분 = d.get("구분", "신규")
            if 구분 == "기존참조":
                print(f"  ✓ {d['진단명']} ({d.get('상태', '')}) — 기존 참조, 새로 저장 안 함")
            elif 구분 == "변경감지":
                print(f"  ⚠ {d['진단명']} ({d.get('상태', '')}) — 변경 감지")
                if d.get("비고"):
                    print(f"    비고: {d['비고']}")
                if input("  → 저장할까요? (y/n): ").strip().lower() == "y":
                    확인된_진단.append(d)
            else:  # 신규
                print(f"  + {d['진단명']} ({d.get('상태', '')}) — 신규")
                if d.get("비고"):
                    print(f"    비고: {d['비고']}")
                if input("  → 저장할까요? (y/n): ").strip().lower() == "y":
                    확인된_진단.append(d)
    if 확인된_진단:
        확인결과["진단"] = 확인된_진단

    # --- 검사결과 ---
    검사들 = 분석결과.get("검사결과", [])
    확인된_검사 = []
    if 검사들:
        # 기존참조 표시
        기존참조 = [k for k in 검사들 if k.get("구분") == "기존참조"]
        if 기존참조:
            print("\n [검사결과 - 기존참조]")
            for k in 기존참조:
                print(f"  ✓ {k['검사항목']} {k.get('결과값', '')} ({k.get('검사시행일', '')}) — 이미 저장됨, 건너뜀")

        # 변경감지 확인
        변경감지 = [k for k in 검사들 if k.get("구분") == "변경감지"]
        if 변경감지:
            print("\n [검사결과 - 변경감지]")
            for k in 변경감지:
                기존값 = k.get("기존값", "?")
                print(f"  ⚠ {k['검사항목']}: 기존 {기존값} → 새 {k.get('결과값', '')} — ", end="")
                if input("수정할까요? (y/n): ").strip().lower() == "y":
                    확인된_검사.append(k)

        # 신규 확인
        신규 = [k for k in 검사들 if k.get("구분") == "신규"]
        if 신규:
            print("\n [검사결과 - 신규]")
            for k in 신규:
                단위 = f" {k.get('단위', '')}" if k.get('단위') else ""
                print(f"  + {k['검사항목']} {k.get('결과값', '')}{단위} ({k.get('검사시행일', '')})")
            if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
                확인된_검사.extend(신규)

    if 확인된_검사:
        확인결과["검사결과"] = 확인된_검사

    # --- 영상검사 ---
    영상들 = 분석결과.get("영상검사", [])
    확인된_영상 = []
    if 영상들:
        기존참조 = [e for e in 영상들 if e.get("구분") == "기존참조"]
        if 기존참조:
            print("\n [영상검사 - 기존참조]")
            for e in 기존참조:
                print(f"  ✓ {e['검사종류']}: {e.get('결과요약', '')} ({e.get('검사시행일', '')}) — 이미 저장됨, 건너뜀")

        신규 = [e for e in 영상들 if e.get("구분") in ("신규", "변경감지")]
        if 신규:
            print("\n [영상검사 - 신규]")
            for e in 신규:
                print(f"  + {e['검사종류']}: {e.get('결과요약', '')} ({e.get('검사시행일', '')})")
            if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
                확인된_영상.extend(신규)

    if 확인된_영상:
        확인결과["영상검사"] = 확인된_영상

    # --- 추적계획 ---
    추적들 = 분석결과.get("추적계획", [])
    확인된_추적 = []
    if 추적들:
        신규 = [p for p in 추적들 if p.get("구분") != "기존참조"]
        if 신규:
            print("\n [추적계획 - 신규]")
            for p in 신규:
                print(f"  + {p.get('내용', '')} 예정: {p.get('예정일', '')}")
            if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
                확인된_추적.extend(신규)

    if 확인된_추적:
        확인결과["추적계획"] = 확인된_추적

    # --- 처방요약 ---
    처방 = 분석결과.get("처방요약", "")
    if 처방:
        print(f"\n [처방요약] {처방}")
        if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
            확인결과["처방요약"] = 처방

    return 확인결과


