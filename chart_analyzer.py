# 핵심 함수 3: 분석결과 저장
# ============================================
def 분석결과_저장(환자id, 방문id, 확인된결과):
    """동의된 항목을 유형별로 적절한 테이블에 저장한다."""
    저장건수 = 0

    # --- 진단 저장 ---
    for d in 확인된결과.get("진단", []):
        진단추가(
            환자id,
            방문id,
            d["진단명"],
            d.get("상태", "활성"),
            d.get("비고", ""),
            d.get("표준코드", None)
        )
        저장건수 += 1
        print(f"  → 진단 저장: {d['진단명']} ({d.get('상태', '')})")

    # --- 검사결과 저장 ---
    for k in 확인된결과.get("검사결과", []):
        검사결과추가(
            환자id,
            k.get("검사시행일", ""),
            k["검사항목"],
            k.get("결과값", ""),
            k.get("단위", ""),
            k.get("참고범위", "")
        )
        저장건수 += 1
        print(f"  → 검사결과 저장: {k['검사항목']} {k.get('결과값', '')}")

    # --- 영상검사 저장 ---
    for e in 확인된결과.get("영상검사", []):
        영상검사추가(
            환자id,
            e.get("검사시행일", ""),
            e["검사종류"],
            e.get("결과요약", ""),
            e.get("주요수치", "")
        )
        저장건수 += 1
        print(f"  → 영상검사 저장: {e['검사종류']}")

    # --- 추적계획 저장 ---
    for p in 확인된결과.get("추적계획", []):
        추적계획추가(
            환자id,
            방문id,
            p.get("예정일", ""),
            p.get("내용", "")
        )
        저장건수 += 1
        print(f"  → 추적계획 저장: {p.get('내용', '')}")

    # --- 검토 의견 → 유형별 저장 ---
    주의메모들 = []
    for 의견 in 확인된결과.get("검토의견", []):
        유형 = 의견.get("유형", "추천")

        if 유형 == "주의":
            # 주의 → 방문 free_text에 메모 추가 (나중에 일괄 저장)
            메모 = f"[AI 주의] {의견.get('내용', '')}"
            if 의견.get("근거"):
                메모 += f" (근거: {의견['근거']})"
            주의메모들.append(메모)
            저장건수 += 1
            print(f"  → 주의사항 메모: {메모[:50]}...")

        elif 유형 == "추적":
            # 추적 → 추적계획 완료 처리
            from util import 추적계획완료
            추적id = 의견.get("추적id")
            if 추적id:
                추적계획완료(추적id)
                저장건수 += 1
                print(f"  → 추적계획 완료 처리: 추적id:{추적id}")
            else:
                print(f"  → 추적계획 완료 처리 실패: 추적id 없음")

        else:
            # 누락, 추천 → 추적계획에 추가
            내용 = f"[AI {유형}] {의견.get('내용', '')}"
            if 의견.get("권장"):
                내용 += f" → {의견['권장']}"
            elif 의견.get("근거"):
                내용 += f" (근거: {의견['근거']})"
            추적계획추가(환자id, 방문id, "", 내용)
            저장건수 += 1
            print(f"  → 추적계획 저장: {내용[:50]}...")

    # 주의 메모를 방문 free_text에 이어붙이기
    if 주의메모들 and 방문id:
        from util import DB연결
        conn = DB연결()
        try:
            기존 = conn.execute(
                "SELECT free_text FROM 방문 WHERE 방문id = ?", (방문id,)
            ).fetchone()
            기존텍스트 = 기존["free_text"] if 기존 and 기존["free_text"] else ""
            구분선 = "\n---\n" if 기존텍스트 else ""
            새텍스트 = 기존텍스트 + 구분선 + "\n".join(주의메모들)
        finally:
            conn.close()
        방문기록수정(방문id, "free_text", 새텍스트)

    return 저장건수
