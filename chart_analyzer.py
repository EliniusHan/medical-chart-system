# 핵심 함수 2: 분석결과 확인 (추출 데이터 + 검토 의견 통합)
# ============================================
def 분석결과_확인(분석결과):
    """AI 분석 결과(추출 데이터 + 검토 의견)를 의사에게 보여주고, 동의한 항목만 반환한다."""
    확인결과 = {}

    # 추출데이터와 검토의견 분리
    추출 = 분석결과.get("추출데이터", 분석결과)  # 하위호환: 추출데이터 키가 없으면 전체를 추출로
    검토의견들 = 분석결과.get("검토의견", [])

    print("\n=== AI 분석 결과 ===")

    # ──────────────────────────────────────
    # Part 1: 추출 데이터
    # ──────────────────────────────────────
    print("\n── 추출 데이터 ──")

    # --- 활력징후 ---
    활력 = 추출.get("활력징후", {})
    if any(활력.get(k) for k in ["수축기", "이완기", "심박수", "키", "몸무게"]):
        bp = f"BP {활력.get('수축기', '?')}/{활력.get('이완기', '?')}-{활력.get('심박수', '?')}"
        체중 = f", 체중 {활력.get('몸무게', '?')}kg" if 활력.get('몸무게') else ""
        키표시 = f", 키 {활력.get('키', '?')}cm" if 활력.get('키') else ""
        bmi = f", BMI {활력.get('BMI', '?')}" if 활력.get('BMI') else ""
        print(f"\n [활력징후] {bp}{키표시}{체중}{bmi}")
        if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
            확인결과["활력징후"] = 활력

    # --- 생활습관 ---
    생활 = 추출.get("생활습관", {})
    if any(생활.get(k) for k in ["흡연", "음주", "운동"]):
        항목들 = []
        if 생활.get("흡연"):
            항목들.append(f"흡연: {생활['흡연']}")
        if 생활.get("음주"):
            항목들.append(f"음주: {생활['음주']}")
        if 생활.get("운동"):
            항목들.append(f"운동: {생활['운동']}")
        print(f"\n [생활습관] {', '.join(항목들)}")
        if input(" → 저장할까요? (y/n): ").strip().lower() == "y":
            확인결과["생활습관"] = 생활

    # --- 진단 ---
    진단들 = 추출.get("진단", [])
    확인된_진단 = []
    if 진단들:
        print("\n [진단]")
        for d in 진단들:
            구분 = d.get("구분", "신규")
            if 구분 == "기존참조":
                print(f"  ✓ {d['진단명']} ({d.get('상태', '')}) — 기존 참조, 새로 저장 안 함")
            elif 구분 == "변경감지":
                print(f"  ⚠ {d['진단명']} ({d.get('상태', '')}) — 변경 감지")
                if d.get("비고"):
                    print(f"    비고: {d['비고']}")
                if input("  → 저장할까요? (y/n): ").strip().lower() == "y":
                    확인된_진단.append(d)
            else:  # 신규
                print(f"  + {d['진단명']} ({d.get('상태', '')}) — 신규")
                if d.get("비고"):
                    print(f"    비고: {d['비고']}")
                if input("  → 저장할까요? (y/n): ").strip().lower() == "y":
                    확인된_진단.append(d)
    if 확인된_진단:
        확인결과["진단"] = 확인된_진단

    # --- 검사결과 ---
    검사들 = 추출.get("검사결과", [])
    확인된_검사 = []
    if 검사들:
        기존참조 = [k for k in 검사들 if k.get("구분") == "기존참조"]
        if 기존참조:
            print("\n [검사결과 - 기존참조]")
            for k in 기존참조:
                print(f"  ✓ {k['검사항목']} {k.get('결과값', '')} ({k.get('검사시행일', '')}) — 이미 저장됨, 건너뜀")

        변경감지 = [k for k in 검사들 if k.get("구분") == "변경감지"]
        if 변경감지:
            print("\n [검사결과 - 변경감지]")
            for k in 변경감지:
                기존값 = k.get("기존값", "?")
                print(f"  ⚠ {k['검사항목']}: 기존 {기존값} → 새 {k.get('결과값', '')} — ", end="")
                if input("수정할까요? (y/n): ").strip().lower() == "y":
                    확인된_검사.append(k)

        신규 = [k for k in 검사들 if k.get("구분") == "신규"]
        if 신규:
            print("\n [검사결과 - 신규]")
