# 핵심 함수 1: 차트 분석
# ============================================
def 차트분석(환자id, free_text):
    """의사의 free-text를 AI가 분석하여 정형화된 JSON으로 반환한다."""
    기록 = 환자전체기록조회(환자id)
    if not 기록:
        return None

    # 나이 추가
    나이 = 나이계산(기록["환자"]["생년월일"])
    if 나이 is not None:
        기록["환자"]["나이"] = f"{나이}세"

    프롬프트 = f"""[기존 환자 데이터]
{json.dumps(기록, ensure_ascii=False, indent=2)}

[오늘 free-text]
{free_text}

위 free-text를 분석하여 기존 데이터와 비교한 뒤, 지정된 JSON 형식으로 출력하세요."""

    응답 = client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=4096,
        temperature=0.1,
        system=SYSTEM_PROMPT,
        messages=[
            {"role": "user", "content": 프롬프트}
        ]
    )

    응답텍스트 = 응답.content[0].text.strip()

    # 마크다운 코드블록 제거 (```json ... ```)
    if 응답텍스트.startswith("```"):
        lines = 응답텍스트.split("\n")
        lines = [l for l in lines if not l.strip().startswith("```")]
        응답텍스트 = "\n".join(lines)

    try:
        return json.loads(응답텍스트)
    except json.JSONDecodeError:
        print(f" [오류] AI 응답을 JSON으로 파싱할 수 없습니다.")
        print(f" 원본 응답:\n{응답텍스트[:500]}")
        return None


