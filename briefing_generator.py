def 브리핑생성_최근차트(환자id):
    """가장 최근 방문의 free_text만으로 브리핑을 생성한다.
    이전 기록을 차트에 요약해서 적는 스타일의 의사용."""
    기록 = 환자전체기록조회(환자id)
    if not 기록:
        return None

    방문목록 = 기록.get("방문", [])
    if not 방문목록:
        return None

    # 가장 최근 방문 찾기 (방문일 기준 내림차순)
    최근방문 = sorted(방문목록, key=lambda v: str(v.get("방문일", "")), reverse=True)[0]
    free_text = 최근방문.get("free_text", "")
    if not free_text or not free_text.strip():
        return None

    # 나이 계산
    환자 = 기록["환자"]
    나이 = 나이계산(환자["생년월일"])
    if 나이 is not None:
        환자["나이"] = f"{나이}세"

    # 최근 차트 + 환자 기본정보만 전송
    최근기록 = {
        "환자": 기록["환자"],
        "최근방문": 최근방문,
    }

    # 익명화
    익명기록, 매핑 = api_익명화(최근기록)

    응답 = api_재시도(lambda: client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=2048,
        temperature=0.1,
        system=SYSTEM_PROMPT_LATEST,
        messages=[
            {"role": "user", "content": f"다음 환자의 가장 최근 진료 기록으로 브리핑을 작성해주세요.\n\n{json.dumps(익명기록, ensure_ascii=False, indent=2)}"}
        ]
    ))

    if not 응답:
        매핑 = None
        return None
    브리핑 = api_복원(응답.content[0].text, 매핑)
    매핑 = None
    return 브리핑


def 브리핑생성(환자id):
    """환자id로 전체 기록을 조회하고 Claude API로 브리핑을 생성한다."""
    기록 = 환자전체기록조회(환자id)
