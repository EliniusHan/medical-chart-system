# 딕셔너리 = 환자 차트 한 장
환자1 = {
    "이름": "김철수",
    "나이": 58,
    "진단": "고혈압",
    "수축기": 145,
    "이완기": 92
}

환자2 = {
    "이름": "이영희",
    "나이": 45,
    "진단": "당뇨",
    "수축기": 128,
    "이완기": 82
}

# 리스트 = 환자 목록 (차트 여러 장을 모은 서류철)
환자목록 = [환자1, 환자2]

# 반복문: 환자 한 명씩 꺼내서 브리핑
for 환자 in 환자목록:
    print("=== 환자 브리핑 ===")
    print("이름:", 환자["이름"])
    print("나이:", 환자["나이"], "세")
    print("진단:", 환자["진단"])
    print("혈압:", 환자["수축기"], "/", 환자["이완기"], "mmHg")

    if 환자["수축기"] >= 140:
        print("⚠ 고혈압 주의")
    elif 환자["수축기"] >= 130:
        print("⚠ 고혈압 전단계")
    else:
        print("혈압 정상")

    print("")  # 환자 사이에 빈 줄