# 함수: "혈압 판정"을 해주는 세트
def 혈압판정(수축기, 이완기):
    if 수축기 >= 180 or 이완기 >= 120:
        return "!!!!고혈압성 응급!!!!즉시 조치 요함!!!!"
    elif 수축기 >=140 or 이완기 >= 90:
        return "2기 고혈압. 치료를 요함."
    elif 수축기 >= 130 or 이완기 >= 80:
        return "1기 고혈압, 식습관 조졀을 요함."
    elif 수축기 >= 120:
        return "주의: 혈압 상승. 정기적 추적을 요함."
    else:
        return "정상 혈압"
    
# 함수: 환자 브리핑용 세트
def 환자브리핑(환자):
    print("=== 환자 브리핑 ===")
    print("이름:", 환자["이름"])
    print("나이:", 환자["나이"],"세")
    print("진단:", 환자["진단"])
    print("혈압:", 환자["수축기"], "/", 환자["이완기"], "mmHg")
    print("판정:", 혈압판정(환자["수축기"], 환자["이완기"]))
    print("")

#환자목록
환자목록 = []

#환자 등록 시스템
print("환자 등록 시스템")
print("환자 등록을 끝내려면 이름에 '끝'을 입력하십시오.")
print("")
while True:
    이름 = input("환자 이름: ")
    if 이름 == "끝":
        break
    나이 = int(input("나이: "))
    진단 = input("진단명: ")
    수축기 = int(input("수축기 혈압: "))
    이완기 = int(input("이완기 혈압: "))

    환자 = {
        "이름": 이름,
        "나이": 나이,
        "진단": 진단,
        "수축기": 수축기,
        "이완기": 이완기
    }

    환자목록.append(환자)
    print("환자 등록 완료! (현재", len(환자목록), "명)")
    print("")

#전체 브리핑 출력
print("")
print("=== 오늘의 전체 환자 브리핑 ===")
print("총", len(환자목록), "명")
print("")

for i, 환자 in enumerate(환자목록, 1):
    print(f"[{i}번 환자]")
    환자브리핑(환자)