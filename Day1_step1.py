# 환자 정보를 변수에 저장하기
환자이름 = "김철수"
나이 = 58
진단명 = "고혈압"
혈압_수축기 = 145
혈압_이완기 = 92

#화면에 출력하기
print("=== 환자 브리핑 ===")
print("이름:", 환자이름)
print("나이:", 나이, "세")
print("진단:", 진단명)
print("혈압:", 혈압_수축기, "/", 혈압_이완기, "mmHg")

#조건문: 혈압에 따라 다른 메시지
if 혈압_수축기 >= 140:
    print("!주의: 수축기 혈압이 140 이상입니다.")
else:
    print("혈압 정상 범위입니다.")